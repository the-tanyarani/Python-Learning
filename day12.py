# String slicing and operations on string 
# 1. Length of a string 
name= "Tanya Rani"
print(len(name))

# 2. String as an array:- A string is like an array so 
           #  we can access the elements of this array.
name= "Tanya"
print(name[0:4])
print(name[1:4]) #including 1 but not 4
print(name[0:-3]) #-3 means (length of string - 3)

